#include <stdio.h>
#include <stdlib.h>

/**
 * add - fn
 * @a: params
 * @b: params
 * Return: nothing
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - fn
 * @a: params
 * @b: params
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - fn
 * @a: params
 * @b: params
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a *b);
}

/**
 * mod - fn
 * @a: params
 * @b: params
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
